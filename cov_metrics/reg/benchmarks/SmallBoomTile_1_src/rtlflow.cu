    cudaError_t checkCuda(cudaError_t result) {
        if (result != cudaSuccess) {
            using namespace std::literals::string_literals;
            throw std::runtime_error("CUDA Runtime Error: "s + cudaGetErrorString(result));
        }
        return result;
    }
    
